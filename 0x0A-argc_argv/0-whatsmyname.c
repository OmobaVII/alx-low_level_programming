#include <stdlib.h>
#include <stdio.h>

/**
 * main - a function used to print the name of the program
 * @argc: number of arguments
 * @argv: the pointer to the argument
 * Return: Success(0)
 */

int main(int argc, char *argv[])
{
	int a;
	(void)argc;

	a = 0;

	printf("%s\n", argv[a]);
	return (0);
}
